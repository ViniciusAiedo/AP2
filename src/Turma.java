public class Turma {


    private int Id;
    private Professor Prof;
    private Cadeira Disciplina;

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public Professor getProf() {
        return Prof;
    }

    public void setProf(Professor prof) {
        Prof = prof;
    }

    public Cadeira getDisciplina() {
        return Disciplina;
    }

    public void setDisciplina(Cadeira disciplina) {
        Disciplina = disciplina;
    }

}
